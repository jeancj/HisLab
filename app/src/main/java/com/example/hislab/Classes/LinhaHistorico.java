package com.example.hislab.Classes;

public class LinhaHistorico {

    private String idHistorico;
    private String idExame;
    private String exame;
    private String data;
    private String valor;
    private String refInf;
    private String refSup;

    public String getIdHistorico() {
        return idHistorico;
    }

    public void setIdHistorico(String idHistorico) {
        this.idHistorico = idHistorico;
    }

    public String getIdExame() {
        return idExame;
    }

    public void setIdExame(String idExame) {
        this.idExame = idExame;
    }

    public String getExame() {
        return exame;
    }

    public void setExame(String exame) {
        this.exame = exame;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    public String getRefInf() {
        return refInf;
    }

    public void setRefInf(String refInf) {
        this.refInf = refInf;
    }

    public String getRefSup() {
        return refSup;
    }

    public void setRefSup(String refSup) {
        this.refSup = refSup;
    }
}
